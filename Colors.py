class Colers:
    @staticmethod
    def all_colers():
        pink = "\033[95m"
        reset = "\033[0m"
        blue = "\033[94m"
        neonblue = "\033[96m"
        Yellow = "\033[93m"
        gray = "\033[90m"
        red = "\033[91m"
        highlite= "\033[100m"
        neongreen = "\033[92m"

        return pink, reset, blue, neonblue, Yellow, gray, red, highlite, neongreen